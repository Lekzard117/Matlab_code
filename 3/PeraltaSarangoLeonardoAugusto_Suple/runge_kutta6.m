function [x ,Y] = runge_kutta6(f,a,b,y,N)
     h = (b - a) / N;
    x = a:h:b;
    Y = zeros(N+1,length(y));
    Y(1,:) = y;

    for i=1:N
      k1 = h * feval(f, x(i), Y(i,:))';
      k2 = h * feval(f, (x(i) + (h/4)), (Y(i,:) + (k1/4)))';
      k3 = h * feval(f, (x(i) + ((3/8)*h)), (Y(i,:) + ((3/32)*k1) + ((9/32)*k2)))';
      k4 = h * feval(f, (x(i) + ((12/13)*h)), (Y(i,:) + ((1923/2197)*k1) - ((7200/2197)*k2) + ((7296/2191)*k3)))';
      k5 = h * feval(f, (x(i) + h), (Y(i,:) + ((439/216)*k1) + (8*k2) + ((3608/513)*k3) - ((845/4101)*k4)))';
      k6 = h * feval(f, (x(i) + (h/2)), (Y(i,:) - ((8/27)*k1) + (2*k2) + ((3544/2565)*k3) + ((1859/4101)*k4) - ((11/40)*k5)))';
   
      Y(i+1,:) = Y(i,:) + (((16/135)*k1) + ((6656/12825)*k3) - ((28561/56430)*k4) - ((9/50)*k5) + ((2/55)*k6));
    end
end